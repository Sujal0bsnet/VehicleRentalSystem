    private void rnoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rnoKeyPressed
        // TODO add your handling code here:
//        String rnov = rno.getText();
        if(evt.getKeyCode()== KeyEvent.VK_ENTER){
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                 conn = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/Rental","root","@pokchu(xumu)0");
                 String rnov = rno.getText();
                ps = conn.prepareStatement("SELECT RegistrationNo, CustomerId, Due ,DATEDIFF(NOW(),Due) AS Elapped FROM rentcar WHERE RegistrationNo = ?");
                ps.setString(1,rnov);
                rs  = ps.executeQuery();
                if(rs.next()== false){
                    JOptionPane.showMessageDialog(this,"Car Id Not Found");
                }else{
                    String custid = rs.getString("CustomerId");
                    cid.setText(custid.trim());
                    String date1 = rs.getString("Due");
                    date.setText(date1.trim());
                    String elp = rs.getString("Elapped");
                    int elaped = Integer.parseInt(elp);
                    if(elaped>0){
                        exdate.setText(elp); 
                        int fine = elaped*100;
                        finetxt.setText(String.valueOf(fine));
                    }else{
                        exdate.setText("0");
                        finetxt.setText("0");
                    }
                }
                     
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ReturnPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }      
    }//GEN-LAST:event_rnoKeyPressed

