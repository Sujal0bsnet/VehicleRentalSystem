    public void Update_Table(){
        int v;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/ReturnPage","root","@pokchu(xumu)0");
             ps = conn.prepareStatement("select * from returnvehic");
             rs = ps.executeQuery();
             ResultSetMetaData rd = rs.getMetaData();
             v = rd.getColumnCount();
             DefaultTableModel df = (DefaultTableModel)returntable.getModel();
             df.setRowCount(0);
             while (rs.next()){
                 Vector t = new Vector();
                 for(int i=1; i<=v; i++){
                     t.add(rs.getString("RegistrationNo"));
                     t.add(rs.getString("CustomerId"));
                     t.add(rs.getString("ReturnDate"));
                     t.add(rs.getString("Elapped"));
                     t.add(rs.getString("Fine"));
                 }
                df.addRow(t);         
             }      
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Customer_Page.class.getName()).log(Level.SEVERE, null, ex);
        }
        rno.setText("");
        cid.setText("");
        date.setText("");
        exdate.setText("");
        finetxt.setText("");
    } 
